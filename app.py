import streamlit as st
import pandas as pd

st.set_page_config(page_title="ระบบตรวจ ปพ.5 อัตโนมัติ", page_icon="📑", layout="wide")

# ==========================================
# 🗄️ โซนที่ 1: แผงควบคุมด้านข้าง (Sidebar)
# ==========================================
with st.sidebar:
    st.header("⚙️ 1. ข้อมูลการส่งงาน")
    
    # 1.1 เลือกกลุ่มสาระ
    department = st.selectbox("เลือกกลุ่มสาระการเรียนรู้:", 
                              ["สังคมศึกษา ศาสนา และวัฒนธรรม", "คณิตศาสตร์", "วิทยาศาสตร์และเทคโนโลยี", "ภาษาต่างประเทศ"])
    
    # 1.2 จำลองรายชื่อครู (ในระบบจริง เราดึงจาก Google Sheets มาโชว์ได้)
    if department == "สังคมศึกษา ศาสนา และวัฒนธรรม":
        teacher_list = ["นายพีรวัฒน์ แสงเงิน", "นางสมหญิง รักเรียน", "นายใจดี มีสุข"]
    else:
        teacher_list = ["ครูท่านอื่นๆ 1", "ครูท่านอื่นๆ 2"]
        
    teacher_name = st.selectbox("เลือกชื่อผู้สอน:", teacher_list)
    
    # 1.3 เลือกรอบการส่ง
    round_choice = st.radio("ระบุรอบการส่งคะแนน:", 
                            ["รอบ 1 (ก่อนกลางภาค)", "รอบ 3 (หลังกลางภาค)", "รอบ 4 (ปลายภาค/สรุปผล)"])

# ==========================================
# 📄 โซนที่ 2: พื้นที่อัปโหลดไฟล์ (Main Area)
# ==========================================
st.title("📑 ระบบตรวจสอบ ปพ.5 อัตโนมัติ (SGS vs NextSchool)")
st.markdown(f"**ผู้ส่ง:** {teacher_name} | **กลุ่มสาระฯ:** {department} | **ส่งงาน:** {round_choice}")
st.markdown("---")

st.subheader("📥 2. อัปโหลดไฟล์ข้อมูล")
col1, col2 = st.columns(2)

with col1:
    file_sgs = st.file_uploader("1️⃣ อัปโหลดไฟล์ ปพ.5 (จาก SGS)", type="pdf")
with col2:
    file_nextschool = st.file_uploader("2️⃣ อัปโหลดไฟล์คะแนน (จาก NextSchool)", type=["pdf", "xlsx", "csv"])

# ==========================================
# 📊 โซนที่ 3: จำลองการแสดงผลและปุ่มบันทึก
# ==========================================
if file_sgs and file_nextschool:
    st.markdown("---")
    st.subheader("ประมวลผลการตรวจสอบ...")
    
    # (โค้ดส่วนดึงข้อมูลและตรวจสอบกฎวิชาการ จะอยู่ตรงนี้...)
    
    # สมมติว่าโปรแกรมตรวจเสร็จแล้ว และได้ผลลัพธ์ว่า "ผ่านทั้งหมด"
    is_approved = True  # เปลี่ยนเป็น False เพื่อดูว่าปุ่มเซฟจะหายไป
    
    if is_approved:
        st.success("✅ ข้อมูลถูกต้องสมบูรณ์ 100% ไม่พบจุดทศนิยม และคะแนนตรงกับ NextSchool")
        
        # 🌟 ไฮไลต์: ปุ่มบันทึกลง Sheet จะโชว์ขึ้นมาเมื่อผ่านเท่านั้น
        st.markdown("### 💾 3. ยืนยันการส่งข้อมูล")
        if st.button("🚀 บันทึกข้อมูลลง Google Sheets ของฝ่ายวิชาการ", type="primary"):
            # (โค้ดยิงข้อมูลเข้า Sheet จะอยู่ตรงนี้)
            st.balloons() # สั่งจุดพลุฉลอง
            st.success(f"บันทึกข้อมูลของ {teacher_name} ลงระบบเรียบร้อยแล้ว!")
    else:
        st.error("🛑 พบข้อผิดพลาดร้ายแรง กรุณาแก้ไขไฟล์แล้วอัปโหลดใหม่อีกครั้ง")